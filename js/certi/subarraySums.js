function findSum(numbers, queries) {
    // console.log({numbers, queries});
    let l = queries.length;
    for(let i=0; i< l ; i++) {
        let sum, j;
        
        const x1=queries[i][1];
        const x2=queries[i][2];
        
        sum = 0;
        j = queries[i][0] - 1;
        
        while(j< x1){
            let num = numbers[j];
            sum += (num == 0) ? x2 : num;
            j++;
        }
        queries[i] = sum;
        
    }
    
    return queries;
}

